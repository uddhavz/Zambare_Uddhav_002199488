    private void btnAddAsPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAsPatientActionPerformed
        // TODO add your handling code here:

        if (selectedPerson.getName() == null) {
            JOptionPane.showMessageDialog(null, "Please select a person from the table first", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (isAlreayAdded()) {
                JOptionPane.showMessageDialog(this, "Person already in Patient list",  "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                addPersonAsPatient();
                selectedPerson = new Person();
                tblPersons.clearSelection();
                JOptionPane.showMessageDialog(this, "Person successfully added as Patient");
            }
        }
    }//GEN-LAST:event_btnAddAsPatientActionPerformed

