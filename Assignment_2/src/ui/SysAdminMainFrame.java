    private void allowOnlyDigitValidation(java.awt.event.KeyEvent evt) {
        char pressedKey = evt.getKeyChar();
        
        if(Character.isAlphabetic(pressedKey)) {
            evt.consume();
        }
    }
    
    // To check every validation of all fields
    private boolean isValidData() {
        boolean isValid = true;
        
        if (txtSYS.getText().isBlank()) {
            isValid = false;
            hideShowValidation(lblSYSValidation, "Please enter Blood pressure SYS");
            
        } 
         
        if (txtDIA.getText().isBlank()) {
            isValid = false;
            hideShowValidation(lblDIAValidation, "Please enter Blood pressure DIA");
            
        } 
        
        if (txtOxygenLevel.getText().isBlank()) {
            isValid = false;
            hideShowValidation(lblOxygenLevelValidation, "Please enter Oxygen level");
            
        }
        
        if (txtRespiratoryRate.getText().isBlank()) {
            isValid = false;
            hideShowValidation(lblRespiratoryRateValidation, "Please enter Respiratory rate");
            
        } 
        
        if (txtBodyTemperature.getText().isBlank()) {
            isValid = false;
            hideShowValidation(lblBodyTemperatureValidation, "Please enter Body temperature");
            
        } 
        
        return isValid;
    }

    // Common function to hide show validation labels
    private void hideShowValidation(JLabel lbl, String errorMessage) {
        if (errorMessage.isBlank()) {
            lbl.setVisible(false);
        } else {
            lbl.setText(errorMessage);
            lbl.setVisible(true);
        }
        
    }
    
