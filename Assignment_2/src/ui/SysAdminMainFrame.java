    private void addPersonAsPatient() {
        Patient patient = new Patient();
        
        Random r = new Random( System.currentTimeMillis() );
        patient.setPatientID((1 + r.nextInt(2)) * 10000 + r.nextInt(10000));
        
        House house = new House();
        Community community = new Community();
        City city = new City();

        house.setHouseName(selectedPerson.getHouse().getHouseName());        
        community.setCommunityName(selectedPerson.getCommunity().getCommunityName());
        city.setCityName(selectedPerson.getCity().getCityName());

        patient.setHouse(house);
        patient.setCommunity(community);
        patient.setCity(city);
        patient.setName(selectedPerson.getName());
        patient.setAge(selectedPerson.getAge());
        patient.setPhoneNumber(selectedPerson.getPhoneNumber());
        patient.setZipCode(selectedPerson.getZipCode());
        patient.setGender(selectedPerson.getGender());
        
        PatientDirectory.patientDirectory.add(patient);
    }
    
    
