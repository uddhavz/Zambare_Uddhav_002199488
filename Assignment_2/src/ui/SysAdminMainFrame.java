    private void addVitalSigns() {
        VitalSigns vitalSign = new VitalSigns();
        
        vitalSign.setSys(Integer.parseInt(txtSYS.getText()));
        vitalSign.setDia(Integer.parseInt(txtDIA.getText()));
        vitalSign.setOxygenLevel(Integer.parseInt(txtOxygenLevel.getText()));
        vitalSign.setRespiratoryRate(txtRespiratoryRate.getText());
        vitalSign.setBodyTemperature(Double.valueOf(txtBodyTemperature.getText()));
        
        DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy - hh:mm:ss");
        Date date = new Date(); 
        
        vitalSign.setVisitTime(date);
        getAgeGroup(vitalSign);
        selectedPatient.encounterList.add(vitalSign);
        
        JOptionPane.showMessageDialog(this, "Vital signs added successfully");
        tblPatient.clearSelection();
        selectedPatient = new Patient();
        addVisitJPanel.setVisible(false);
    }
    
    public void getAgeGroup(VitalSigns vs){ //Checks the age value enterred by user and assigns the corresponding age group defined in the table
        if (selectedPatient.age < 1){
            vs.setAgeGroup("Infant");
        }
        else if (selectedPatient.age > 1 && selectedPatient.age <= 5){
            vs.setAgeGroup("Toddler");
        }
        else if (selectedPatient.age > 5 && selectedPatient.age <= 13){
            vs.setAgeGroup("Kid");
        }
        else if (selectedPatient.age > 13){
            vs.setAgeGroup("Adult");
        }
    }
    
