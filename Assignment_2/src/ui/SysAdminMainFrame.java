    public void refreshTable(){
//        int rowCount = tblPersons.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblPersons.getModel();
        model.setRowCount(0);
        
        for(Person person: prd.getPersonDirectory()){
            Object row[] = new Object[8];
            row[0] = person.getName();
            row[1] = person.getAge();
            row[2] = person.getPhoneNumber();
            row[3] = person.getCity().getCityName();
            row[4] = person.getZipCode();
            row[5] = person.getCommunity().getCommunityName();
            row[6] = person.getHouse().getHouseName();
            row[7] = person.getGender();
            model.addRow(row);
        }
    }
    
    

