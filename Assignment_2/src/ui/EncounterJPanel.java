    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        Encounter encounter = new Encounter();
        int patientHeartRate = -1;
        int patientO2Level = -1;
        int patientHBloodPressure = -1;
        int patientLBloodPressure = -1;
        float patientTemperature = -1;
        try{
            patientHeartRate = Integer.parseInt(heartRate.getText());
            patientO2Level = Integer.parseInt(oxygenLevel.getText());
            patientTemperature = Float.parseFloat(temperature.getText());
            patientLBloodPressure = Integer.parseInt(lBloodPressure.getText());
            patientHBloodPressure = Integer.parseInt(hBloodPressure.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please provide correct Vital Details!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        encounter.setHeartRate(patientHeartRate);
        encounter.setOxygenLevel(patientO2Level);
        encounter.setTemperature(patientTemperature);
        encounter.sethBloodPressure(patientHBloodPressure);
        encounter.setlBloodPressure(patientLBloodPressure);
        encounter.setVisitDate(new Date());
        system.getPatientDirectory().getPatient(currentPatient.getPatientID()).getEncounterHistory().addEncounter(encounter);
        String successMessage = "Vitals are added successfully!!";
        if(!encounter.isNormal()){
            successMessage+="\nPatients Condition is Abnormal";
        }
        JOptionPane.showMessageDialog(this, successMessage, "Info", JOptionPane.INFORMATION_MESSAGE);

//        Person j mjp = new MainJPanel(layeredPane, system);
//        displayPanel(mjp);

    }//GEN-LAST:event_submitBtnActionPerformed


